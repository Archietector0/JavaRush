public class DrawLines {
    public static void main(String[] args) {
        System.out.println("\n-------VERTICAL_LINE--------");
        for (int i = 0; i < 10; ++i)
            System.out.println("8");

        System.out.println("\n-------GORIZONTAL_LINE--------\n");
        for (int a = 0; a < 10; ++a)
            System.out.print("8");

    }
}